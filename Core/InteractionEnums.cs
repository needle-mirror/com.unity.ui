namespace UnityEngine.UIElements
{
    /// <summary>
    /// Controls how many items can be selected at once.
    /// </summary>
    public enum SelectionType
    {
        /// <summary>
        /// Selections are disabled.
        /// </summary>
        None,
        /// <summary>
        /// Only one item is selectable.
        /// </summary>
        Single,
        /// <summary>
        /// Multiple items are selectable at once.
        /// </summary>
        Multiple
    }
}
