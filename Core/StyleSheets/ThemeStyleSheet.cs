using System;

namespace UnityEngine.UIElements
{
    /// <summary>
    /// Represents a style sheet that's assembled from other style sheets.
    /// </summary>
    [Serializable]
    public class ThemeStyleSheet : StyleSheet
    {
        internal override void OnEnable()
        {
            isDefaultStyleSheet = true;
            base.OnEnable();
        }
    }
}
